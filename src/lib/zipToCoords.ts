export function zipToCoords(zip: string): { lat: number; lng: number; city: string } | null {
    const normalized = zip.replace(/\s+/g, '').substring(0, 5);
    return ZIP_COORDS[normalized] || null;
}

export function isValidZip(zip: string): boolean {
    const normalized = zip.replace(/\s+/g, '');
    return /^\d{5}$/.test(normalized);
}
